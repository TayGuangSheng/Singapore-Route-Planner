export const LANGUAGE_STORAGE_KEY = "route_planner_language";

export const getInitialLanguage = () => {
  const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (saved === "en" || saved === "zh") {
    return saved;
  }
  return "en";
};
